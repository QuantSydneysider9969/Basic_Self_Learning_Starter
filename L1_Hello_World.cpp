// iostream = input, output stream
#include <iostream>

// cout means character out, especially for outputting
// std is the short form of standard library
// int is the data type of function output/variable
int main() {
    std::cout << "Hello World!"; // This line is called statment, telling comp. what to do
    // :: is the scope resolution operator, specifying cout stream inside the standard namespace
    // << is the operator to execute the cout command with value "Hello World!"
    return 0; 
    // return '0' told the comp. the program is gonna terminate after statements, other values
    // told the computer the program encounters error
}